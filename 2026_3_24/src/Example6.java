public class Example6 {
    public static void main(String[] args) throws Exception {
        System.out.println("计算-10的绝对值" + Math.abs(-10));
        System.out.println("求大于5.6的最小整数" + Math.ceil(5.6));
        System.out.println("小于-4.2的最大整数" + Math.floor(-4.2));
        System.out.println("4.6四舍五入" + Math.round(4.6));
        System.out.println("2.1和-2.1较大值" + Math.max(2.1,-2.1));
        System.out.println("2.1和-2.1较小值" + Math.min(2.1, -2.1));
        System.out.println("生成随机数"+ Math.random());
        System.out.println("1.57的sin值" + Math.sin(1.57));
        System.out.println("4的平方根" + Math.sqrt(4));
        System.out.println("2的3次方" + Math.pow(2,3));
    }
}
